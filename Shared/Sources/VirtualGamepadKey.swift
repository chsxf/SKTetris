enum VirtualGamepadKey: Int {
    case dpadLeft   = 0x1
    case dpadRight  = 0x2
    case dpadUp     = 0x4
    case dpadDown   = 0x8
    case buttonA    = 0x10
    case buttonX    = 0x20
    case buttonMenu = 0x40
}
